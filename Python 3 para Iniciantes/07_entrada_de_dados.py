# Entrada de dados pelo teclado

# String
nome = str(input('Digite o seu nome: ')) # cast str
print(nome)
print(type(nome))

print()

# Inteiro
idade = int(input('Digite sua idade: ')) # cast int
print(idade)
print(type(idade))

print()

# Float
peso = float(input('Digite seu peso: ')) # cast float
print(peso)
print(type(peso))