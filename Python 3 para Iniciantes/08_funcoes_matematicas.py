# Funções Matemáticas

import math # módulo math (funções matemáticas)

# math.ceil(n) - Menor inteiro maior ou igual a n
print(math.ceil(6.54))

# math.fabs(n) - Valor absoluto de n
print(math.fabs(-5))

# math.factorial(n) - Fatorial de n
print(math.factorial(6))

# math.floor(n) - Inteiro menor ou igual a n
print(math.floor(6.54))

# math.exp(n) - Exponencial de n
print(math.exp(2))

# math.log(n) - Logarítmo de n
print(math.log(10))

# math.log(n) - Logarítmo de n na base 2
print(math.log2(4))

# math.log(n) - Logarítmo de n na base 10
print(math.log10(10))

# math.pow(x, y) - Exponenciação x, y
print(math.pow(2, 10))

# math.sqrt(n) - Raíz quadrada de n
print(math.sqrt(100))

# math.cos(n) - Cosseno de n
print(math.cos(10))

# math.sin(n) - Cosseno de n
print(math.sin(10))

# math.pi - Valor de pi
print(math.pi)

# math.e - exponencial
print(math.e)