# and -> conjunção
# or -> disjunção
# not -> negação

# negação
var = True
print(var)
print(not var) # negação

print()

# conjunção
v1 = True
v2 = False
r = v1 and v2 # conjunção
print(r)
v3 = True
r = v1 and v3 # conjunção
print(r)

print()

# disjunção
r = v1 or v2 # disjunção
print(r)
v4 = False
r = v2 or v4 # disjunção
print(r)